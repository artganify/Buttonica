using Buttonica.Engine.Framework.Scenes;

namespace Buttonica.Scenes
{
	/// <summary>
	///     Represents the title scene of the game (currently used for testing purposes)
	/// </summary>
	public class TitleScene : Scene
	{
		/// <summary>
		///     Invoked when the scene is being activated
		/// </summary>
		protected override void OnActivate()
		{
			base.OnActivate();
		}

		/// <summary>
		///     Invoked when the scene is being deactivated
		/// </summary>
		protected override void OnDeactivate()
		{
			base.OnDeactivate();
		}

		/// <summary>
		///     Invoked during disposal of managed resources
		/// </summary>
		protected override void OnManagedDisposal()
		{
			base.OnManagedDisposal();
		}

		/// <summary>
		///     Invoked during disposal of unmanaged resources
		/// </summary>
		protected override void OnUnmanagedDisposal()
		{
			base.OnUnmanagedDisposal();
		}
	}
}